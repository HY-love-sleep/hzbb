package com.hy.common;

/**
 * Description: 链表
 * Author: yhong
 * Date: 2023/12/14
 */
public class ListNode {
    public int val;
    public ListNode next;

    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }


}
